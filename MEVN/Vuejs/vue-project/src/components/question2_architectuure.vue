MVVM architecture:
MMVM stands for Model View View-Model,
MMVM is a pattern that separates the graphical UI from logic. 

View: 
View is the actual data object model . It uses DOM. View layer is responsible for displaying data.

View-Model:
It is an object that syncs the model and view. Every vue instance is a view model. 
It consists of DOM Listener and Data Binding. 

Model:
Model layer is responsible for storing of data.